// Do While Loop
// Struktur kontrol dalam pemrograman yang memastikan blok kode dieksekusi setidaknya satu kali

public class Main {
    public static void main(String[] args) {
        int index = 1;
        do {
            System.out.println("Ini Looping Ke" + " " + index);
            index++;
        }
        while (index <= 5);
    }
}
