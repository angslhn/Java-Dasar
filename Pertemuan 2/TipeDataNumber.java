public class TipeDataNumber {
    public static void main(String[] args) {
//  Ini adalah tipe data angka bulat.
        byte iniByte = 100;
        short iniShort = 7310;
        int iniInt = 100000;
        long iniLong = 1000000;
        long iniLong2 = 1000000000000L;

//  Ini adalah tipe data angka desimal.
        float iniFloat = 3.14F;
        double iniDouble = 2.3721;

//  Ini adalah kode literals
        int decimalInt = 25;
        int hexInt = 0xFFFFFF;
        int binInt = 0b00001010;

//  Ini adalah cara memudahkan membaca angka nol.
        int harga = 1_000_000_000;
        long price = 1_000_000_000_000L;
    }
}
