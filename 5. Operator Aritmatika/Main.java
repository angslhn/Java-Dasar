// Operator Aritmatika

//  +  Penjumlahan Menjumlahkan dua nilai
//  -  Pengurangan Mengurangi satu nilai dari nilai lain
//  *  Perkalian Mengalikan dua nilai
//  /  Pembagian Membagi satu nilai dengan nilai lain
//  %  Modulus Mengembalikan sisa pembagian
//  ++ Penambahan Menambah nilai variabel sebesar 1
//  -- Pengurangan Menurunkan nilai variabel sebesar 1

public class Main {
    public static void main(String[] args) {
        int hasil_tambah = 5 + 10;
        int hasil_kurang = 32 - 20;
        int hasil_kali = 165 * 42;
        int hasil_bagi = 32 / 8;
        int hasil_modulus = 8 % 2;

        System.out.println(hasil_tambah);
        System.out.println(hasil_kurang);
        System.out.println(hasil_kali);
        System.out.println(hasil_bagi);
        System.out.println(hasil_modulus);
    }
}