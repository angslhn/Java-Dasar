// Tipe data

//  Jenis non-primitive dapat digunakan untuk memanggil metode untuk melakukan operasi tertentu, sedangkan tipe primitive tidak bisa

//  Tipe data primitive
//  byte      Menyimpan bilangan bulat dari -128 hingga 127
//  short     Menyimpan bilangan bulat dari -32.768 hingga 32.767
//  int       Menyimpan bilangan bulat dari -2.147.483.648 hingga 2.147.483.647
//  long      Menyimpan bilangan bulat dari -9.223.372.036.854.775.808 hingga 9.223.372.036.854.775.807
//  float     Menyimpan bilangan pecahan. Cukup untuk menyimpan 6 hingga 7 digit desimal
//  double    Menyimpan bilangan pecahan. Cukup untuk menyimpan 15 hingga 16 digit desimal
//  boolean   Menyimpan nilai true atau false
//  char      Menyimpan satu karakter/huruf atau nilai ASCII

//  Tipe data non-primitive
//  String    Digunakan untuk merepresentasikan teks.

public class Main {
    public static void main(String[] args) {
        String nama = "Mamat Rahmat";
        String email = "mamat.rahmat@icloud.com";
        String no_handphone = "082345531124";

        System.out.println("Nama         = " + nama);
        System.out.println("Emai         = " + email);
        System.out.println("No Handphone = " + no_handphone);
    }
}